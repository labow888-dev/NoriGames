/* ══════ HELPERS ══════ */
const rooms = {};
const shuffle = arr => [...arr].sort(() => Math.random() - .5);
const rankScore = r => [100, 75, 50][r] ?? 25;

function getFirstChar(word) {
  // Return first meaningful Thai character or first char
  const trimmed = word.trim();
  if (!trimmed) return '?';
  // For Thai, just return the first unicode character
  return [...trimmed][0];
}

function broadcastRoom(code) {
  const room = rooms[code]; if (!room) return;
  Object.values(room.players).forEach(p =>
    io.to(p.id).emit('updateRoom', buildView(room, p.id))
  );
}

function buildView(room, pid) {
  const c = JSON.parse(JSON.stringify(room));
  delete c.masterWords;
  if (c.players[pid]) c.players[pid].word = null;
  return c;
}

function activePlayers(room) {
  return room.turnOrder.filter(id => room.players[id] && !room.players[id].out && !room.players[id].won);
}

function advanceTurn(room) {
  const alive = activePlayers(room);
  if (!alive.length) return null;
  const idx = alive.indexOf(room.currentTurn);
  return alive[(idx + 1) % alive.length];
}

function checkWinCondition(room) {
  const winners = Object.values(room.players).filter(p => p.won).length;
  return winners >= room.maxWinners;
}

/* ══════ SOCKET ══════ */
io.on('connection', socket => {
  console.log('+ connect', socket.id);

  /* ── WebRTC Signaling ── */
  socket.on('webrtc-offer', ({ code, targetId, offer }) => {
    io.to(targetId).emit('webrtc-offer', { fromId: socket.id, offer });
  });
  socket.on('webrtc-answer', ({ code, targetId, answer }) => {
    io.to(targetId).emit('webrtc-answer', { fromId: socket.id, answer });
  });
  socket.on('webrtc-ice', ({ code, targetId, candidate }) => {
    io.to(targetId).emit('webrtc-ice', { fromId: socket.id, candidate });
  });

  /* ── Mic status ── */
  socket.on('micStatus', ({ code, muted }) => {
    const room = rooms[code];
    if (!room || !room.players[socket.id]) return;
    room.players[socket.id].muted = muted;
    broadcastRoom(code);
  });

  /* สร้างห้อง */
  socket.on('createRoom', ({ name, avatar, maxPlayers, maxWrong, maxWinners, category, hintEnabled }) => {
    const code = String(Math.floor(10000 + Math.random() * 90000));
    // Handle random category
    const selectedCategory = category === '__random__'
      ? CATS[Math.floor(Math.random() * CATS.length)]
      : (WORDS[category] ? category : CATS[0]);

    rooms[code] = {
      code, host: socket.id, phase: 'waiting',
      category: selectedCategory,
      isRandomCategory: category === '__random__',
      maxPlayers: Math.min(10, Math.max(2, +maxPlayers || 8)),
      maxWrong:   Math.min(10, Math.max(1, +maxWrong   || 3)),
      maxWinners: Math.min(10, Math.max(1, +maxWinners || 1)),
      hintEnabled: hintEnabled !== false,
      players: { [socket.id]: mkPlayer(socket.id, name, avatar) },
      masterWords: {}, winOrder: [], turnOrder: [], currentTurn: null,
      chatMessages: [],
    };
    socket.join(code);
    socket.data = socket.data || {}; // กำหนดค่าเริ่มต้นกัน Error
    socket.data.roomCode = code;
    socket.emit('roomCreated', { code, view: buildView(rooms[code], socket.id) });
  });

  /* เข้าห้อง */
  socket.on('joinRoom', ({ code, name, avatar }) => {
    const room = rooms[code];
    if (!room)                                               return socket.emit('gameError', 'ไม่พบห้องนี้!');
    if (room.phase !== 'waiting')                            return socket.emit('gameError', 'เกมเริ่มแล้ว!');
    if (Object.keys(room.players).length >= room.maxPlayers) return socket.emit('gameError', 'ห้องเต็มแล้ว!');

    room.players[socket.id] = mkPlayer(socket.id, name, avatar);
    socket.join(code); 
    socket.data = socket.data || {}; // กำหนดค่าเริ่มต้นกัน Error
    socket.data.roomCode = code;
    // Notify existing players about new player (for WebRTC)
    socket.to(code).emit('playerJoined', { newPlayerId: socket.id });
    broadcastRoom(code);
    socket.emit('joinedRoom', { code });
  });

  /* ปรับ settings */
  socket.on('setSettings', ({ code, category, maxPlayers, maxWrong, maxWinners, hintEnabled }) => {
    const room = rooms[code];
    if (!room || room.host !== socket.id) return;
    if (category === '__random__') {
      room.category = CATS[Math.floor(Math.random() * CATS.length)];
      room.isRandomCategory = true;
    } else if (category && WORDS[category]) {
      room.category = category;
      room.isRandomCategory = false;
    }
    if (maxPlayers) room.maxPlayers = Math.min(10, Math.max(2, +maxPlayers));
    if (maxWrong)   room.maxWrong   = Math.min(10, Math.max(1, +maxWrong));
    if (maxWinners) room.maxWinners = Math.min(10, Math.max(1, +maxWinners));
    if (hintEnabled !== undefined) room.hintEnabled = hintEnabled !== false;
    broadcastRoom(code);
    io.to(code).emit('toast', { msg: `📂 หมวดหมู่: ${room.category}`, type: 'i' });
  });

  /* preview ห้อง (ก่อนเข้า) */
  socket.on('previewRoom', code => {
    const room = rooms[code];
    if (!room || room.phase !== 'waiting') return;
    socket.emit('roomPreview', {
      maxPlayers: room.maxPlayers,
      maxWrong: room.maxWrong,
      maxWinners: room.maxWinners,
      category: room.category,
      hintEnabled: room.hintEnabled !== false,
    });
  });

  /* สุ่มหมวดหมู่ใหม่ (host เท่านั้น) */
  socket.on('randomCategory', code => {
    const room = rooms[code];
    if (!room || room.host !== socket.id) return;
    room.category = CATS[Math.floor(Math.random() * CATS.length)];
    room.isRandomCategory = true;
    broadcastRoom(code);
    io.to(code).emit('toast', { msg: `🎲 สุ่มได้: ${room.category}`, type: 'i' });
  });

  /* kick ผู้เล่น */
  socket.on('kickPlayer', ({ code, targetId }) => {
    const room = rooms[code];
    if (!room || room.host !== socket.id || targetId === socket.id) return;
    io.to(targetId).emit('kicked');
    delete room.players[targetId];
    broadcastRoom(code);
  });

  /* เริ่มเกม */
  socket.on('startGame', code => {
    const room = rooms[code];
    if (!room || room.host !== socket.id) return;
    const players = Object.values(room.players);
    if (players.length < 2) return socket.emit('gameError', 'ต้องมีอย่างน้อย 2 คน!');

    // Cap maxWinners to player count
    if (room.maxWinners > players.length) room.maxWinners = players.length;

    const wl = shuffle(WORDS[room.category] || WORDS[CATS[0]]);
    room.masterWords = {}; room.winOrder = [];
    room.turnOrder = shuffle(players.map(p => p.id));
    room.currentTurn = room.turnOrder[0];
    room.chatMessages = []; // reset chat on new game

    players.forEach((p, i) => {
      p.word = wl[i % wl.length];
      p.wrong = 0; p.out = false; p.won = false; p.rank = null; p.score = 0; p.hintUsed = false;
      room.masterWords[p.id] = p.word;
    });
    room.phase = 'playing';
    broadcastRoom(code);
    io.to(code).emit('toast', { msg: `🎲 เริ่ม! หมวด: ${room.category} | รอบแรก: ${room.players[room.currentTurn]?.name}`, type: 'i' });
  });

  /* ตอบคำ */
  socket.on('answer', ({ code, answer }) => {
    const room = rooms[code];
    if (!room || room.phase !== 'playing') return;
    if (room.currentTurn !== socket.id) return socket.emit('gameError', 'ยังไม่ถึงรอบคุณ!');
    const p = room.players[socket.id];
    if (!p || p.out || p.won) return;

    if (answer.trim() === room.masterWords[socket.id]) {
      const rank = room.winOrder.length;
      room.winOrder.push(socket.id);
      p.rank = rank; p.score = rankScore(rank);
      p.totalScore = (p.totalScore || 0) + p.score;
      p.won = true; p.out = true;
      io.to(code).emit('toast', { msg: `${'🥇🥈🥉'.split('')[rank] ?? '🎖️'} ${p.name} ตอบถูก! +${p.score}pt`, type: 's' });

      // Check win condition
      if (checkWinCondition(room)) {
        endGame(room, code); return;
      }
    } else {
      p.wrong++;
      if (p.wrong >= room.maxWrong) {
        p.out = true;
        io.to(code).emit('toast', { msg: `💀 ${p.name} หมดสิทธิ์!`, type: 'e' });
      } else {
        socket.emit('toast', { msg: `❌ ผิด! (${p.wrong}/${room.maxWrong})`, type: 'e' });
      }
    }

    if (Object.values(room.players).every(pl => pl.out || pl.won)) {
      endGame(room, code); return;
    }
    room.currentTurn = advanceTurn(room);
    broadcastRoom(code);
    if (room.currentTurn) io.to(code).emit('toast', { msg: `➡️ ถึงรอบ ${room.players[room.currentTurn]?.name}`, type: 'i' });
  });

  /* ขอเฉลย (hint) - เฉลยตัวแรกเท่านั้น */
  socket.on('requestHint', code => {
    const room = rooms[code];
    if (!room || room.phase !== 'playing') return;
    if (room.currentTurn !== socket.id) return socket.emit('gameError', 'ยังไม่ถึงรอบคุณ!');
    const p = room.players[socket.id];
    if (!p || p.out || p.won) return;
    if (room.hintEnabled === false) return socket.emit('gameError', 'ห้องนี้ปิดระบบเฉลย!');
    // Only allow hint on last chance
    if (p.wrong < room.maxWrong - 1) return socket.emit('gameError', 'ยังใช้เฉลยไม่ได้!');
    if (p.hintUsed) return socket.emit('gameError', 'ใช้เฉลยไปแล้ว!');

    const word = room.masterWords[socket.id];
    const hint = getFirstChar(word);
    p.hintUsed = true;
    socket.emit('hintResult', { hint });
    io.to(code).emit('toast', { msg: `💡 ${p.name} ขอเฉลย!`, type: 'i' });
    broadcastRoom(code);
  });

  /* ยอมแพ้ */
  socket.on('surrender', code => {
    const room = rooms[code];
    if (!room || room.phase !== 'playing') return;
    const p = room.players[socket.id];
    if (!p || p.out || p.won) return;

    p.out = true;
    io.to(code).emit('toast', { msg: `🏳️ ${p.name} ยอมแพ้! (คำคือ: ${room.masterWords[socket.id]})`, type: 'e' });

    if (Object.values(room.players).every(pl => pl.out || pl.won)) {
      endGame(room, code); return;
    }
    if (room.currentTurn === socket.id) {
      room.currentTurn = advanceTurn(room);
      if (room.currentTurn) io.to(code).emit('toast', { msg: `➡️ ถึงรอบ ${room.players[room.currentTurn]?.name}`, type: 'i' });
    }
    broadcastRoom(code);
  });

  /* ข้าม */
  socket.on('skip', code => {
    const room = rooms[code];
    if (!room || room.phase !== 'playing' || room.currentTurn !== socket.id) return;
    room.currentTurn = advanceTurn(room);
    broadcastRoom(code);
    if (room.currentTurn) io.to(code).emit('toast', { msg: `⏭️ ถึงรอบ ${room.players[room.currentTurn]?.name}`, type: 'i' });
  });

  /* แชท */
  socket.on('chat', ({ code, text }) => {
    const room = rooms[code];
    if (!room || !room.players[socket.id]) return;
    const msg = {
      id: Date.now(), from: socket.id,
      name: room.players[socket.id].name,
      avatar: room.players[socket.id].avatar,
      text: String(text).slice(0, 120),
    };
    room.chatMessages.push(msg);
    if (room.chatMessages.length > 200) room.chatMessages.shift();
    io.to(code).emit('chatMsg', msg);
  });

  /* เล่นอีกรอบ */
  socket.on('playAgain', code => {
    const room = rooms[code];
    if (!room || room.host !== socket.id) return;
    Object.values(room.players).forEach(p => {
      p.word = null; p.wrong = 0; p.out = false; p.won = false; p.rank = null; p.score = 0; p.hintUsed = false;
    });
    room.masterWords = {}; room.winOrder = []; room.turnOrder = []; room.currentTurn = null;
    room.chatMessages = []; // reset chat
    room.phase = 'waiting';
    // If random category, re-roll
    if (room.isRandomCategory) {
      room.category = CATS[Math.floor(Math.random() * CATS.length)];
    }
    broadcastRoom(code);
  });

  /* disconnect */
  socket.on('disconnect', () => {
    // ใช้ Optional Chaining (?.) เพื่อป้องกัน Error ถ้า socket.data ไม่มีค่า
    const code = socket.data?.roomCode; 
    if (!code) return; // ออกทันทีถ้ายูสเซอร์นั้นไม่ได้อยู่ในห้องใดเลย

    const room = rooms[code]; if (!room) return;
    delete room.players[socket.id];
    delete room.masterWords[socket.id];
    room.turnOrder = room.turnOrder.filter(id => id !== socket.id);

    const remaining = Object.keys(room.players);
    if (!remaining.length) { delete rooms[code]; return; }
    if (room.host === socket.id) room.host = remaining[0];
    if (room.currentTurn === socket.id) room.currentTurn = advanceTurn(room);

    if (room.phase === 'playing') {
      if (checkWinCondition(room) || Object.values(room.players).every(p => p.out || p.won)) {
        endGame(room, code); return;
      }
    }
    broadcastRoom(code);
  });
});

/* ══════ GAME END ══════ */
function endGame(room, code) {
  room.phase = 'result';
  const rv = JSON.parse(JSON.stringify(room));
  delete rv.masterWords;
  Object.entries(room.masterWords || {}).forEach(([pid, w]) => {
    if (rv.players[pid]) rv.players[pid].word = w;
  });
  io.to(code).emit('updateRoom', rv);
  io.to(code).emit('toast', { msg: `🏁 เกมจบแล้ว!`, type: 's' });
}

function mkPlayer(id, name, avatar) {
  return { id, name, avatar: avatar||null, word:null, wrong:0, out:false, won:false, totalScore:0, rank:null, score:0, hintUsed:false, muted:false };
}

